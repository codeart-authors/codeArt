# This file defines a trainer class RabertTrainer, which extends the Trainer 
# class from transformers.

# The RabertTrainer should overwrite several methods that would report multiple
# losses.

# The model output is defined as follows:

# class MaskedLMWithEdgePredOutput(ModelOutput):

#     loss: Optional[torch.FloatTensor] = None
#     masked_lm_loss: Optional[torch.FloatTensor] = None
#     edge_prediction_loss: Optional[torch.FloatTensor] = None
#     logits: torch.FloatTensor = None
#     ep_logits: torch.FloatTensor = None
#     hidden_states: Optional[Tuple[torch.FloatTensor]] = None
#     attentions: Optional[Tuple[torch.FloatTensor]] = None

# Besides `loss`, `masked_lm_loss` and `edge_prediction_loss` should also be
# reported to Tensorflow.

